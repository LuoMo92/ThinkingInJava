package com.luomo.tij.chapter2.practice9;

/**
 * @author LiuMei
 * @date 2018-03-29
 */
public class AutoboxTest {

    public static void main(String[] args) {
        boolean b = false;
        char c = 'c';
        byte bt = 8;
        short sh = 16;
        int i = 32;
        long l = 64;
        float f = 0.32f;
        double d = 0.64;
        Boolean bl = b;
        System.out.println("boolean b:" + b);
        System.out.println("Boolean bl:" + bl);
        Character character = c;
        System.out.println("char c:" + c);
        System.out.println("Character character:" + character);
        Byte byt = bt;
        System.out.println("byte bt:" + bt);
        System.out.println("Byte byt:" + byt);
        Short shor = sh;
        System.out.println("short:" + sh);
        System.out.println("Short shor:" + shor);
        Integer integer = i;
        System.out.println("int i:" + i);
        System.out.println("Integer integer:" + integer);
        Long lo = l;
        System.out.println("long l:" + l);
        System.out.println("Long lo:" + lo);
        Float fl = f;
        System.out.println("float f:"+f);
        System.out.println("Float fl:"+fl);
        Double db = d;
        System.out.println("double d:"+d);
        System.out.println("Double db:"+db);
    }
}
